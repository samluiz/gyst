@Composable
fun rememberStrings(preferredLanguage: String?): AppStrings {
    val localeLang = Locale.current.language
    return remember(preferredLanguage, localeLang) {
        val lang =
            when (preferredLanguage) {
                "pt", "en" -> preferredLanguage
                else -> localeLang.lowercase()
            }
        appStringsForLanguage(lang)
    }
}

internal fun appStringsForLanguage(language: String): AppStrings =
    when (language.lowercase()) {
        "pt" -> ptBr
        else -> enUs
    }
